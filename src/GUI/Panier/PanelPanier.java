private void loadProducts() {
        ligneCommands = new LigneCommandeDAO().findLigneCommandByPanier(panier.getId());
        if (ligneCommands.size() > 0) {
            panelProduit.setPreferredSize(new Dimension(100, ligneCommands.size() * 182));
            int x = 10;
            int y = 100;
            for (int i = 0; i < ligneCommands.size(); i++) {
                //ajout du bouton pour la supprission
                lDelete.add(new JLabel(new ImageIcon("images\\closeEntred.png")));
                panelProduit.add(lDelete.get(i));
                lDelete.get(i).setBounds(x, y + 20, 30, 20);
                //Ajout de l'image de produit
                Produit p = new ProduitDAO().findProduitById(ligneCommands.get(i).getProduit().getId());
                String imagePath = "src\\com\\images\\" + p.getNom() + ".png";
                ImageIcon icon1 = new ImageIcon(new ImageIcon(imagePath).getImage().
                        getScaledInstance(80, 70, Image.SCALE_DEFAULT));
                lImage.add(new JLabel(icon1));
                panelProduit.add(lImage.get(i));
                lImage.get(i).setBounds(x + 50, y, 80, 70);
                //Ajout du nom du produit
                lNom.add(new JLabel(p.getNom()));
                panelProduit.add(lNom.get(i));
                lNom.get(i).setBounds(x + 170, y + 20, 90, 30);
                //Ajout du Qte
                lQte.add(new JSpinner(new SpinnerNumberModel(ligneCommands.get(i).getQte(), 1, 10, 1)));
                panelProduit.add(lQte.get(i));
                lQte.get(i).setBounds(x + 300, y + 20, 50, 30);
                //Ajout du prix unitaire
                lPrix.add(new JLabel( p.getPrix()+" DT"));
                panelProduit.add(lPrix.get(i));
                lPrix.get(i).setBounds(x + 400, y + 20, 60, 30);
                //Ajout de la promotion
                lPromotion.add(new JLabel(p.getPromotion()+" %"));
                panelProduit.add(lPromotion.get(i));
                lPromotion.get(i).setBounds(x + 500, y + 20, 50, 30);
                //Ajout du prix unitaire
                float total = p.getPrix() * ligneCommands.get(i).getQte();
                if (p.getPromotion() != 0) {
                    total = (p.getPrix() - (p.getPrix() * p.getPromotion() / 100)) * ligneCommands.get(i).getQte();
                }
                lTotal.add(new JLabel(total+" DT"));
                panelProduit.add(lTotal.get(i));
                lTotal.get(i).setBounds(x + 600, y + 20, 60, 30);
                //Ajout du separateur
                separateur.add(new JSeparator());
                panelProduit.add(separateur.get(i));
                separateur.get(i).setBounds(0, y + 82, 670, 10);
                y += 100;
            }

        }
        Actions();
        calculerTotal();
    }

    private void Actions() {
        for (int i = 0; i < ligneCommands.size(); i++) {
            int j = i;
            lDelete.get(i).addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    deleteMouseClicked(evt, j);
                }

                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    deleteMouseEntered(evt, j);
                }

                @Override
                public void mouseExited(java.awt.event.MouseEvent evt) {
                    deleteMouseExited(evt, j);
                }

            });
                    lImage.get(i).addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    imageMouseClicked(evt, j);
                }

                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    imageMouseEntered(evt, j);
                }

                @Override
                public void mouseExited(java.awt.event.MouseEvent evt) {
                    imageMouseExited(evt, j);
                }

            });

        }
    }

    private void deleteMouseClicked(MouseEvent evt, int i) {
        new LigneCommandeDAO().removeLigneCommandeById(ligneCommands.get(i).getId());
        ligneCommands = new LigneCommandeDAO().findLigneCommandByPanier(panier.getId());
        f.setText("" + ligneCommands.size());
        removeAll();
        repaint();
        revalidate();
        initComponents();
        loadProducts();
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
        calculerTotal();

    }

    private void deleteMouseEntered(MouseEvent evt, int j) {
        setCursor(new Cursor(Cursor.HAND_CURSOR));

    }

    private void deleteMouseExited(MouseEvent evt, int j) {
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }
    
    
