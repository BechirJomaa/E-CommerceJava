    private void calculerTotal(){
        prixAP=0;
        prixToatl=0;
        ligneCommands.stream().map((ligneCommand) -> {
            prixAP += (ligneCommand.getQte() * ligneCommand.getProduit().getPrix());
            return ligneCommand;
        }).forEach((ligneCommand) -> {
            if (ligneCommand.getProduit().getPromotion() != 0) {
                prixToatl += (ligneCommand.getProduit().getPrix() - (ligneCommand.getProduit().getPrix() * ligneCommand.getProduit().getPromotion() / 100)) * ligneCommand.getQte();
            } else {
                prixToatl += (ligneCommand.getQte() * ligneCommand.getProduit().getPrix());
            }
        });
        lPrixAP.setText(""+prixAP+" DT");
        lPrixTotal.setText(""+prixToatl+" DT");
    }
