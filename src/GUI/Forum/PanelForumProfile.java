private void loadSujets() {
        listSujets = new SujetDAO().findSujetByUser(userId);
        lTitre = new JLabel[listSujets.size()];
        lModifier = new JLabel[listSujets.size()];
        lSupprimer = new JLabel[listSujets.size()];
        JLabel[] lDescription = new JLabel[listSujets.size()];
        JLabel[] lUser = new JLabel[listSujets.size()];
        lid = new Integer[listSujets.size()];
        int x = 210;
        int y = 100;
        for (int i = 0; i < listSujets.size(); i++) {
            // ajout du titre
            lTitre[i] = new JLabel(listSujets.get(i).getTitre());
            lTitre[i].setBounds(x, y, 130, 25);
            lTitre[i].setForeground(Color.black);
            lTitre[i].setFont(new java.awt.Font("Tahoma", 1, 20));
            add(lTitre[i]);
            //ajout de la description
            lDescription[i] = new JLabel(listSujets.get(i).getSujet());
            lDescription[i].setBounds(x + 200, y, 290, 25);
            lDescription[i].setForeground(Color.black);
            lDescription[i].setFont(new java.awt.Font("Tahoma", 2, 14));
            add(lDescription[i]);
            //ajout de l'id du sujet
            lid[i] = listSujets.get(i).getId();
            //ajout de la boutton modifier
            lModifier[i] = new JLabel("Modifier");
            lModifier[i].setBounds(x + 550, y, 90, 25);
            lModifier[i].setForeground(Color.white);
            lModifier[i].setFont(new java.awt.Font("Tahoma", 2, 14));
            lModifier[i].setOpaque(true);
            lModifier[i].setBackground(Color.blue);
            add(lModifier[i]);
            //ajout de la boutton supprimer
            lSupprimer[i] = new JLabel("Supprimer");
            lSupprimer[i].setBounds(x + 650, y, 90, 25);
            lSupprimer[i].setForeground(Color.white);
            lSupprimer[i].setFont(new java.awt.Font("Tahoma", 2, 14));
            lSupprimer[i].setOpaque(true);
            lSupprimer[i].setBackground(Color.blue);
            y += 60;
            add(lSupprimer[i]);
        }

    }

 private void sujetActions() {
        for (int i = 0; i < lTitre.length; i++) {
            int j = i;
            lTitre[i].addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    titreMouseClicked(evt, j);
                }

                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    titreMouseEntered(evt, j);
                }

                @Override
                public void mouseExited(java.awt.event.MouseEvent evt) {
                    titreMouseExited(evt, j);
                }

            });
            lModifier[i].addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    modifierMouseClicked(evt, j);
                }

                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    modifierMouseEntered(evt, j);
                }

                @Override
                public void mouseExited(java.awt.event.MouseEvent evt) {
                    modifierMouseExited(evt, j);
                }

            });
            lSupprimer[i].addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseClicked(java.awt.event.MouseEvent evt) {
                    supprimerMouseClicked(evt, j);
                }

                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    supprimerMouseEntered(evt, j);
                }

                @Override
                public void mouseExited(java.awt.event.MouseEvent evt) {
                    supprimerMouseExited(evt, j);
                }

            });

        }
    }

    private void titreMouseClicked(MouseEvent evt, int i) {
        removeAll();
        repaint();
        revalidate();
        PanelSujetDisplay p = new PanelSujetDisplay(lid[i], userId);
        p.setBounds(0, 0, 1310, 500);
        add(p);
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));

    }

    private void titreMouseEntered(MouseEvent evt, int j) {
        lTitre[j].setForeground(Color.BLUE);
        setCursor(new Cursor(Cursor.HAND_CURSOR));

    }

    private void titreMouseExited(MouseEvent evt, int j) {
        lTitre[j].setForeground(Color.black);
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }
     
    private void modifierMouseClicked(MouseEvent evt, int i) {
        removeAll();
        repaint();
        revalidate();
        PanelModifierSujet p = new PanelModifierSujet(lid[i],userId);
        p.setBounds(0, 0, 1310, 500);
        add(p);
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));

    }

    private void modifierMouseEntered(MouseEvent evt, int j) {
        setCursor(new Cursor(Cursor.HAND_CURSOR));

    }

    private void modifierMouseExited(MouseEvent evt, int j) {
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }
    
    private void supprimerMouseClicked(MouseEvent evt, int i) {
        removeAll();
        repaint();
        revalidate();
        new SujetDAO().removeSujetById(lid[i]);
        initComponents();
        loadSujets();
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));

    }

    private void supprimerMouseEntered(MouseEvent evt, int j) {
        setCursor(new Cursor(Cursor.HAND_CURSOR));

    }

    private void supprimerMouseExited(MouseEvent evt, int j) {
        setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
    }

