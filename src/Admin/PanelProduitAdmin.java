                add(lPromotion.get(i));
                lPromotion.get(i).setBounds(x + 430, y + 20, 50, 30);
                //Ajout du categories        
                lbCategories.add(new JLabel(p.getCategorie_id().getNom()));
                add(lbCategories.get(i));
                lbCategories.get(i).setBounds(x + 530, y + 20, 90, 30);
                //Ajout du bouuton plus de dÃ©tails
